  <tr>
    <td valign="top">3.</td>
    <td valign="top">Setelah melakukan pengisian User ID dan password, maka BNI Internet Banking Personal akan menampilkan beberapa pilihan menu, seperti Menu Informasi Rekening, Menu Transfer Dana, Menu Pembayaran, Menu Pembelian, Menu Simulasi, dan Menu Administrasi. Untuk melakukan pembayaran universitas, pilih Menu Pembayaran. <br />
Dalam Menu Pembayaran, terdapat beberapa pilihan menu, seperti Menu Pembayaran Kartu Kredit, Menu Pembayaran Telepon/Seluler, Menu Pembayaran Listrik, Menu Pembayaran Penerbangan, Menu Pembayaran Biaya Pendidikan, Menu Pembayaran Pinjaman/Loan, Menu Pembayaran ZIS dan Qurban, Menu Pembayaran  TV Berlangganan, dan Menu Pembayaran Pajak. Untuk melakukan pembayaran universitas, pilih Menu Pembayaran Biaya Pendidikan.<br />
<br />
<img width="466" height="371" src="internet_clip_image002_0001.jpg" alt="3.jpg" /></td>
  </tr>
  <tr>
    <td valign="top">4.</td>
    <td valign="top">Untuk melakukan pembayaran biaya pendidikan perguruan tinggi/universitas, maka mahasiswa/i melakukan proses sbb :<br />
&bull;	 Pilih nama perguruan tinggi, yaitu Institut Pertanian Bogor  (IPB)<br />
&bull;	 Masukkan nomor tagihan (penulisan nomor tagihan tanpa<br />
karakter khusus)<br />
&bull;	 Pilih nomor rekening yang digunakan untuk melakukan pembayaran<br />
&bull;	 Sebelum melakukan pembayaran, aktifkan terlebih dahulu token BNI Internet Banking (BNI E-Secure) dengan mengisi password yang dimiliki oleh mahasiswa/i<br />
&bull;	 Seteah BNI E-Secure aktif, klik tombol kunci yang tertera pada layar, maka akan muncul 8 (delapan) nomor yang harus diketik pada BNI E-Secure milik mahasiswa/i<br />
&bull;	 Maka akan muncul 8 (delapan) nomor respon yang muncul pada BNI E-Secure milik mahasiswa/i yang harus diketik pada kolom yang tersedia pada halaman BNI Internet Banking Personal. <br />
&bull;	Klik, Lanjutkan<br />
<br />
<img width="503" height="351" src="internet_clip_image002_0002.jpg" alt="4.jpg" /></td>

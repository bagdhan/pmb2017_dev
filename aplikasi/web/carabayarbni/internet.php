    <td valign="top">2.</td>
    <td valign="top">Halaman BNI Internet Banking akan menampilan menu awal BNI Internet Banking Personal. Untuk login, mahasiswa/i harus mempunyai user ID dan password BNI Internet Banking Personal. Pembayaran universitas merupakan transaksi finansial, oleh karena itu mahasiswa/i harus mengisi user ID dan password yang telah didaftarkan sebelumnya melalui cabang BNI.<br />
    <br />
    <img width="390" height="265" src="internet_clip_image002_0000.jpg" alt="2.jpg" /></td>

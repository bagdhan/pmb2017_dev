  <tr>
    <td valign="top">5.</td>
    <td valign="top">Pada halaman BNI Internet Banking Personal, akan muncul informasi pembayaran yang akan diproses. Informasi tersebut adalah seperti Nama Universitas, Kode Perguruan Tinggi, Nomor Tagihan, Nama Mahasiswa, Nomor Mahasiswa, Jumlah Pembayaran (Rp), Rekening Pembayar, dan 8 (delapan) nomor yang harus diketik pada BNI E-Secure milik mahasiswa/i. Setelah memasukkan 8 (delapan) nomor tersebut, maka mahasiswa/i harus mengetik 8 (delapan) nomor respon yang muncul pada BNI E-Secure pada kolom yang tersedia. <br />
Pastikan data yang diinput oleh mahasiwa/i telah benar dikarenakan BNI tidak bertanggung jawab atas kesalahan input data. Untuk melakukan pembayaran biaya pendidikan universitas, klik lanjutkan. <br />
<br />
<img width="486" height="348" src="internet_clip_image002_0003.jpg" alt="6.jpg" /></td>
  </tr>
  <tr>
    <td valign="top">6.</td>
    <td valign="top">BNI Internet Banking Personal akan menampilan halaman pemberitahuan bahwa pembayaran biaya pendidikan universitas telah BERHASIL dilakukan. Informasi yang tertera pada halaman BNI Internet Banking Personal adalah Nama Universitas, Kode Perguruan Tinggi, Nomor Tagihan, Nama Mahasiswa, Nomor Mahasiswa, Jumlah Pembayaran (Rp), Rekening Pembayar, Periode Pembayaran, Nomor Referensi, Tanggal/Waktu Pembayaran, Nomor Jurnal Bank, Nomor Registrasi Bank, dan Status Pembayaran. Untuk mencetak bukti pembayaran, klik cetak bukti pembayaran dan untuk menyimpan bukti pembayaran, klik simpan bukti pembayaran. <br />

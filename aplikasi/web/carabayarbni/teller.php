<h3>Tata Cara Pembayaran Mahasiswa Melalui Teller</h3>
  <ol>
    <li>Mahasiswa/i mendatangi cabang BNI terdekat </li>
    <li>Pembayaran mahasiswa/i dapat dilakukan secara tunai atau pemindahbukuan (transfer)</li>
    <li>Mahasiswa menginformasikan identifikasi biller (no.billing / nomor mahasiswa) kepada teller</li>
    <li>Teller akan melakukan verifikasi atas informasi pembayaran mahasiswa/i </li>
    <li>Mahasiswa/i melakukan pembayaran</li>
    <li>Mahasiswa/i akan mendapatkan Tanda Terima Pembayaran Mahasiswa (Figure 1). Berikut ini adalah Tanda Terima Pembayaran Mahasiswa yang terdiri dari informasi seperti Nama Institusi, Nomor Pembayaran, Nomor Pokok Mahasiswa, Nama Mahasiswa, Fakultas, Jurusan, Program Studi, Nomor Registrasi, Total Pembayaran, Nomor Transaksi, dan Pengesahan Petugas Bank. <br />
      
                        </li>
  </ol>

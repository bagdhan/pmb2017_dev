<table width="100%">
  <tr>
    <td width="1%" valign="top">1.</td>
    <td width="99%" valign="top">Mahasiswa/i mendatangi ATM terdekat</td>
  </tr>
  <tr>
    <td valign="top">2.</td>
    <td valign="top">Mahasiswa/i memasukkan kartu ATM ke dalam mesin ATM BNI (pembayaran melalui ATM tidak dapat dilakukan melalui ATM bank lain)<br />
    <p><img src="atm_clip_image002.jpg" alt="2" width="383" height="239" /></p></td>
  </tr>
  <tr>
    <td valign="top">3.</td>
    <td valign="top">Mahasiswa/i memilih bahasa yang ingin digunakan dalam proses pembayaran. Bahasa yang tersedia adalah bahasa inggris dan bahasa Indonesia.<br />
      <br />
    <img src="atm_clip_image002_0000.jpg" alt="3" width="383" height="239" /></td>
  </tr>
  <tr>
    <td valign="top">4.</td>
    <td valign="top">Mahasiswa/i memasukkan kode pin kartu ATM<br />
    <br />
    <img src="atm_clip_image002_0001.jpg" alt="4" width="383" height="239" /></td>
  </tr>
  <tr>
    <td valign="top">5.</td>
    <td valign="top">Mesin ATM akan menampilkan beberapa menu pilihan seperti Menu Penggantian PIN, Menu Transfer, Menu Pembayaran, Menu Registrasi E-Channel, Menu Penarikan Tunai, dan Menu Informasi Saldo. Untuk pembayaran universitas, pilih Menu Pembayaran.<br />
    <br />
    <img src="atm_clip_image002_0002.jpg" alt="5" width="383" height="239" /></td>
  </tr>
  <tr>
    <td valign="top">6.</td>
    <td valign="top">Mesin ATM akan menampilkan beberapa menu pembayaran seperti Pembayaran Tiket, Pembayaran Qurban, Pembayaran Kartu Debit BNI, Pembayaran Air Minum, Pembayaran Telepon/HP, Pembayaran Kartu Kredit, Pembayaran Pinjaman, dan Pilihan Menu Berikutnya. Untuk pembayaran universitas, pilih Menu Berikutnya.<br />
    <br />
    <img src="atm_clip_image002_0003.jpg" alt="6" width="383" height="239" /></td>
  </tr>
  <tr>
    <td valign="top">7.</td>
    <td valign="top">Mesin ATM akan menampilkan beberapa menu pembayaran seperti Pembayaran Modul Penerimaan Negara, Pembayaran Internet, Pembayaran Universitas, Pembayaran TV Berlangganan, Pembayaran PLN, dan Pilihan Menu Utama. Untuk pembayaran universitas, pilih Universitas.<br />
    <br />
    <img src="atm_clip_image002_0004.jpg" alt="7" width="383" height="239" /></td>
  </tr>
  <tr>
    <td valign="top">8.</td>
    <td valign="top">Mesin ATM akan menampilkan Jenis Pembayaran Universitas, yaitu Menu Pembayaran Student Payment Centre (SPC) dan Pembayaran UI. Untuk pembayaran universitas, pilih Menu Pembayaran Student Payment Centre (SPC).<br />
    <br />
    <img src="atm_clip_image002_0005.jpg" alt="8" width="383" height="239" /></td>
  </tr>
  <tr>
    <td valign="top">9.</td>
    <td valign="top">Mesin ATM akan menampilkan layar untuk memasukkan kode universitas dan nomor tagihan mahasiswa/i. Kode universitas Institut Pertanian Bogor (IPB) untuk Sekolah Pasca Sarjana adalah 9076. Berikut ini adalah contoh melakukan proses pembayaran universitas, yaitu : <br />
      <strong>Kode Universitas 	: 9076</strong><br />
<strong>No Pendaftaran			: 1234567890 <br />
Tekan				: 90761234567890</strong><br />
<br />
<img src="atm_clip_image002_0006.jpg" alt="9" width="383" height="239" /></td>
  </tr>
  <tr>
    <td valign="top">10.</td>
    <td valign="top">Mesin ATM akan menampilkan layar untuk memilih rekening yang akan digunakan mahasiswa/i untuk melakukan pembayaran. Pilihan menu yang tersedia adalah Giro, Tabungan, dan Kartu Kredit. Untuk melakukan pembayaran universitas, pilih Tabungan.<br />
    <br />
    <img src="atm_clip_image002_0007.jpg" alt="10" width="383" height="239" /></td>
  </tr>
  <tr>
    <td valign="top">11.</td>
    <td valign="top">Mesin ATM akan menampilkan layar berisi pemberitahuan bahwa transaksi telah selesai dilakukan dan menawarkan pilihan apabila mahasiswa/i ingin melakukan transaksi yang lain. Pilih Ya apabila mahasiswa/i ingin melakukan transaksi lainnya melalui mesin ATM. Pilih Tidak apabila mahasiswa/i telah selesai melakukan transaksi.<br />
    <br />
    <img src="atm_clip_image002_0008.jpg" alt="11" width="383" height="239" /></td>
  </tr>
  <tr>
    <td valign="top">12.</td>
    <td valign="top">Apabila mahasiswa/I telah selesai melakukan transaksi. Maka mesin ATM akan mengeluarkan struk bukti transaksi pembayaran universitas yang terdiri dari informasi sebagai berikut : Nomor Record, Nomor Mahasiswa, Nama, Nomor Referensi, Nomor Registrasi, dan Jumlah Bayar.<br />
    <br />
    <img src="atm_clip_image002_0009.jpg" alt="12" width="383" height="239" /></td>
  </tr>
